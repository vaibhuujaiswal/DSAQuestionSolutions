class Solution{
    public:
    /* Should return data of middle node. If linked list is empty, then  -1*/
    int getMiddle(Node *head)
    {
        Node *curr = head;
        Node *middle = head;
        int count = -1;
        while (curr != NULL){
            count++;
            curr = curr->next;
        }
        int mid;
        if (count % 2 == 0) mid = count/2;
        else mid = (count + 1)/2;
        
        for (int i = 0; i < mid; i++){
            middle = middle->next;
            if (middle == NULL) return -1;
        }
        return middle->data;
    }
};


